package com.miradry.softwareversion.model;

public class SftwVersion {
 
	private String type;
	private String id;
	private String sftVer;
	private String url;
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getSftVer() {
		return sftVer;
	}
	public void setSftVer(String sftVer) {
		this.sftVer = sftVer;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	
	
}
